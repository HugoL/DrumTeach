<div class="texto">
<p>Utilizar <b>DrumTeach</b> es realmente fácil. Sigue estos pasos:</p>

<div class="clearfix">&nbsp;</div>
<h4>Regístrate</h4>
<p>Mediante el botón "Regístrate gratis" de la página principal accedes al formulario de registro. Rellena los datos y pincha en el botón de enviar.</p>

<div class="clearfix">&nbsp;</div>
<h4>Verifica tu cuenta</h4>
<p>Accede al email que hayas puesto en el formulario de registro. Te habrá llegado un email de DrumTeach. Pincha en el enlace que aparece en el email para que se active tu cuenta.</p>
<div class="clearfix">&nbsp;</div>
<h4>Accede</h4>
<p>Pincha en "Login" en el menú superior. Indica el nombre de usuario que hayas elegido o el email y la contraseña que has introducido al registrarte.</p>

<div class="clearfix">&nbsp;</div>
<h4>Introduce tus ejercicios</h4>
<p><strong>Enhorabuena!</strong> ya estás dentro del panel de usuario. Ahora solo tienes que introducir tus ejercicios. Pincha en "Nuevo ejercicio" en el menú de la izquierda, introduce el nombre del ejercicio, la velocidad a la que lo tienes que desarrollar actualmente y cualquier observación que quieras anotar. Pincha en guardar. </p>
<p>Repite este paso para introducir todos tus ejercicios</p>

<div class="clearfix">&nbsp;</div>
<h4>Consulta tu información</h4>
<p>Ya tienes la información de tus ejercicios guardada. La podrás consultar y modificar durante tu clase de batería, en tu local cuando estés practicando, en tu casa o donde quieras...</p>
<p>Además, DrumTeach controla la fecha en la que modificaste por última vez la velocidad de cada ejercicio y te muestra diferentes alertas para que tengas controlados todos tus ejercicios.</p>

<div class="clearfix">&nbsp;</div>
<p>Si tienes alguna sugerencia o quieres proponer más categorías o cualquier otra cosa, solo tienes que decírmelo a través del formulario de contacto o a través del email que pone en el pie de página.</p>
